def threeSumClosest(nums,target):
    nums.sort()
    currsum=0
    closestsum=float("inf")
    n=len(nums)
    for i in range(0,n):
        for j in range(i+1,n):
            for k in range(j+1,n):
                currsum=nums[i]+nums[j]+nums[k]   
                if currsum==target : return target  
                if abs(closestsum-target)>abs(currsum-target):
                    closestsum=currsum
    return closestsum        

